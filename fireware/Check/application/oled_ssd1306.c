/* 当 I2C 总线连续写失败时，尝试对 I2C 外设软复位以恢复 */
static void Oled_I2C_Reset(void)
{
  (void)HAL_I2C_DeInit(&hi2c1);
  HAL_Delay(5);
  if (HAL_I2C_Init(&hi2c1) != HAL_OK) {
    /* re-init failed - nothing more to do here */
  } else {
    /* re-init ok */
  }
}

#define OLED_I2C_ADDR                     (0x3CU << 1U)
#define OLED_CONTROL_CMD                  0x00U
#define OLED_CONTROL_DATA                 0x40U
#define OLED_PAGE_COUNT                   8U
#define OLED_FONT_WIDTH                   6U
#define OLED_FONT_HEIGHT                  8U

/* 使用最小 5x7 ASCII 字库，优先保证 0.96 寸 OLED 可直接显示业务信息。 */
static uint8_t oled_buffer[OLED_WIDTH * OLED_PAGE_COUNT];

/* 清空显示缓冲区并保留显示器刷新操作由调用者控制 */
void Oled_Clear(void)
{
  memset(oled_buffer, 0, sizeof(oled_buffer));
}

